public class FreqSeqBean {
    private int id;
    private int frequency;
    private String freq_seq;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getFrequency() {
        return frequency;
    }

    public void setFrequency(int frequency) {
        this.frequency = frequency;
    }

    public String getFreq_seq() {
        return freq_seq;
    }

    public void setFreq_seq(String freq_seq) {
        this.freq_seq = freq_seq;
    }
}
